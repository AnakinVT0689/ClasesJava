package contar2en2;

public class Contar2en2 {

    public static void main(String[] args) {
        /*Realizar un programa que muestr por pantalla los numeros
        del 200 al 250 saltando de 2 en 2
        La secuencia deberia ser: 200, 202, 204...*/
        
        int cont=200;
        while (cont <=250){
            System.out.println(cont);
            cont = cont +2;
        }
            
    }
    
}
